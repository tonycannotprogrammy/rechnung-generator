window.t = function(key) {
  // language globally stored in sysSettings.language, defaults to 'de'
  const lang = (window.sysSettings && window.sysSettings.language) || 'de';
  if (window.i18nText[lang] && window.i18nText[lang][key]) {
      return window.i18nText[lang][key];
  }
  return window.i18nText['de'][key] || key; // fallback to German or key
};

window.applyLanguageToDOM = function() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (key === 'APP_TITLE') {
      const lang = (window.sysSettings && window.sysSettings.language) || 'de';
      el.innerHTML = window.i18nText[lang][key];
    } else if (el.tagName === 'INPUT' && el.type === 'text' && el.placeholder) {
      el.placeholder = window.t(key);
    } else {
      el.innerText = window.t(key);
    }
  });
};
